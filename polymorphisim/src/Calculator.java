public class Calculator {
    public int add(int no1, int no2) {
        return no1+no2;
    }
    public float add(float no1, float no2) {
        return no1+no2;
    }
    public int add(int no1, int no2, int no3) {
        return no1+no2+no3;
    }
    public int subtract(int intNUm, double dNum) {
        return intNUm - (int) dNum;
    }

}
